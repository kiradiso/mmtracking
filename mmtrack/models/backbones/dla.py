def get_block(name):
    if name == "dla34":
        return BasicBlock
    elif name == "dla102":
        return Bottleneck
    elif name == "dla46_c":
        Bottleneck.expansion = 2
        return Bottleneck
    elif name == "dla46x_c":
        BottleneckX.expansion = 2
        return BottleneckX
    elif name == "dla60x_c":
        BottleneckX.expansion = 2
        return BottleneckX
    elif name == "dla60":
        Bottleneck.expansion = 2
        return Bottleneck
    elif name == "dla60x":
        BottleneckX.expansion = 2
        return BottleneckX
    elif name == "dla102x":
        BottleneckX.expansion = 2
        return BottleneckX
    elif name == "dla102x2":
        BottleneckX.cardinality = 64
        return BottleneckX
    elif name == "dla169":
        Bottleneck.expansion = 2
        return Bottleneck
    else:
        raise ValueError('{} is invalid'.format(name))

